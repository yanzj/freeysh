#!/bin/bash


for i in `seq 1 5`
do
	echo -e "\033[32m______$i\033[0m"
done

echo -e "\033[31m#########################################################################\033[0m"



result=0
for ((i=1; i<=100; i++))
do
	result=`expr $result + $i`
done

echo $result


echo -e "\033[31m#########################################################################\033[0m"



